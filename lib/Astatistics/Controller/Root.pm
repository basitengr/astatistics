package Astatistics::Controller::Root;
use Moose;
use namespace::autoclean;

BEGIN { extends 'Catalyst::Controller' }

#
# Sets the actions in this controller to be registered with no prefix
# so they function identically to actions created in MyApp.pm
#
__PACKAGE__->config(namespace => '');

=head1 NAME

Astatistics::Controller::Root - Root Controller for Astatistics

=head1 DESCRIPTION

[enter your description here]

=head1 METHODS

=head2 index

The root page (/)

=cut

sub index :Path :Args(0) {
    my ( $self, $c ) = @_;

		if ($c->user_exists()) {
			$c->detach('/stats/show');
		} else {
			$c->detach('/users/login');
		}
}

=head2 default

Standard 404 error page

=cut

sub default :Path {
    my ( $self, $c ) = @_;
		#$c->response->body($c->localize('Page not found'));
		$c->stash->{'template'} = 'error/404.tt';
    $c->stash->{'message'} = $c->localize('Page not found');
    $c->response->status(404);
}

=head2 end

Attempt to render a view, if needed.

=cut

sub end : ActionClass('RenderView') {}

